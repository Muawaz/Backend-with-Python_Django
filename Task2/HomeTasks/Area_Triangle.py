# Find Area of a Triangle
# 1. Write a Python program that takes the base and 
# height of a triangle as input and calculates its area. 
# The formula for the area of a triangle is: Area=1/2xbasexheight

base = float(input("Enter the base of the Triangle = "))
height = float(input("Enter the height of the Triangle = "))

area = 0.5 * base * height

print ("\nThe area of the given Triangle = ", area, "\n")